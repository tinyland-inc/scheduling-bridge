import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const root = new URL('../', import.meta.url);
const checkMode = process.argv.includes('--check');

const read = (relativePath) =>
  readFileSync(new URL(relativePath, root), 'utf8');

const extract = (source, pattern, label) => {
  const match = source.match(pattern);
  if (!match?.[1]) {
    throw new Error(`Unable to find ${label}`);
  }
  return match[1];
};

const extractBlock = (source, pattern, label) => {
  const match = source.match(pattern);
  if (!match?.[1]) {
    throw new Error(`Unable to find ${label}`);
  }
  return match[1];
};

const parseObjectEntries = (block) =>
  Array.from(block.matchAll(/^\s*(\w+): '([^']+)'/gm)).map(([, key, value]) => ({
    key,
    value,
  }));

const parseQuotedList = (block) =>
  Array.from(block.matchAll(/'([^']+)'/g)).map(([, value]) => value);

const pkg = JSON.parse(read('package.json'));
const moduleBazel = read('MODULE.bazel');
const buildBazel = read('BUILD.bazel');
const flakeNix = read('flake.nix');
const healthTs = read('src/server/health.ts');
const ciWorkflow = read('.github/workflows/ci.yml');
const publishWorkflow = read('.github/workflows/publish.yml');
const bazelVersion = read('.bazelversion').trim();

const moduleVersion = extract(
  moduleBazel,
  /module\([\s\S]*?version = "([^"]+)"/m,
  'MODULE.bazel version',
);
const nodeVersion = extract(
  moduleBazel,
  /node\.toolchain\(node_version = "([^"]+)"/,
  'node toolchain version',
);
const pnpmVersion = extract(
  moduleBazel,
  /pnpm_version = "([^"]+)"/,
  'pnpm version',
);
const flakeNodeMajor = extract(
  flakeNix,
  /\bnodejs_(\d+)\b/,
  'flake Node package',
);
const bazelPackageName = extract(
  buildBazel,
  /npm_package\([\s\S]*?package = "([^"]+)"/m,
  'Bazel package name',
);
const bazelPackageVersion = extract(
  buildBazel,
  /npm_package\([\s\S]*?version = "([^"]+)"/m,
  'Bazel package version',
);
const ciBazelTargets = extract(
  ciWorkflow,
  /bazel_targets:\s*"([^"]+)"/,
  'CI Bazel targets',
);
const ciPackageDir = extract(
  ciWorkflow,
  /package_dir:\s*([^\n]+)/,
  'CI package directory',
).trim();
const ciBuildCommand = extract(
  ciWorkflow,
  /build_command:\s*([^\n]+)/,
  'CI build command',
).trim();
const ciNodeMatrix = JSON.parse(
  extract(
    ciWorkflow,
    /node_versions:\s*'(\[[^\n]+\])'/,
    'CI node versions',
  ),
);
const ciPublishNode = extract(
  ciWorkflow,
  /publish_node_version:\s*"([^"]+)"/,
  'CI publish node version',
).trim();
const ciTypecheckCommand = extract(
  ciWorkflow,
  /typecheck_command:\s*([^\n]+)/,
  'CI typecheck command',
).trim();
const ciUnitTestCommand = extract(
  ciWorkflow,
  /unit_test_command:\s*([^\n]+)/,
  'CI unit test command',
).trim();
const publishPackageDir = extract(
  publishWorkflow,
  /package_dir:\s*([^\n]+)/,
  'publish package directory',
).trim();
const publishNpmMode = extract(
  publishWorkflow,
  /npm_publish_mode:\s*([^\n]+)/,
  'publish npm publish mode',
).trim();
const protocolVersion = extract(
  healthTs,
  /BRIDGE_PROTOCOL_VERSION = '([^']+)'/,
  'protocol version',
);
const endpoints = parseObjectEntries(
  extractBlock(
    healthTs,
    /BRIDGE_PROTOCOL_ENDPOINTS = \{([\s\S]*?)\} as const;/m,
    'protocol endpoints',
  ),
);
const capabilities = parseQuotedList(
  extractBlock(
    healthTs,
    /BRIDGE_PROTOCOL_CAPABILITIES = \[([\s\S]*?)\] as const;/m,
    'protocol capabilities',
  ),
);
const exportEntries = Object.entries(pkg.exports).map(([key, value]) => ({
  key,
  types: value.types,
  runtime: value.import ?? value.default,
}));

